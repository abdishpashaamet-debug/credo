            <h2>Vårt mål</h2>
            <div class="hr l"></div>
            <p>Det ukrainske barnekoret i Oslo dyrker fram talenter samtidig som vi fremmer en dyp tilknytning til
                ukrainsk kultur gjennom kormusikk. Gjennom sang, kreativitet og engasjerende musikalske og rytmiske
                aktiviteter, fremmer koret skjønnhet og harmoni. </p>
            <p>Barna synger ukrainske, norske og internasjonale sanger. Det ukrainske barnekoret i Norge fungerer som en
                viktig bro mellom kulturer, og fremmer integrering og gjensidig forståelse. </p>
